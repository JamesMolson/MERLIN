#include <cmath>
#include "AcceleratorModel/Supports/AcceleratorSupport.h"

using namespace std;

double AcceleratorSupport::DistanceTo (const AcceleratorSupport& aSupport) const
{
    Point2D dx = aSupport.pos-pos;
    return sqrt(dx*dx);
}

